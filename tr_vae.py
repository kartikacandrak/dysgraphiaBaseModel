        save_path = os.path.join(output_img_dir, f"best_{f1}")
        cv2.imwrite(save_path, best_data_f1["canvas"])

        best_data_f1["image_path"] = save_path
        del best_data_f1["canvas"]

        results.append(best_data_f1)

