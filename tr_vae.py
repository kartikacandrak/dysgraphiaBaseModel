                    mask_h = cv2.inRange(roi, 0, 110)
                    mask_a = cv2.inRange(roi, 111, 220)

                    M = cv2.moments(mask_h)

                    if M["m00"] == 0:
                        ref_x, ref_y = roi_w // 2, IMG_SIZE // 2
